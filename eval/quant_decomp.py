#!/usr/bin/env python3
"""E-Q1: quantization error DECOMPOSITION for the metric-3D student.

Premise check for "scale-rail quantization" (try-to-disprove first). Hypothesis:
under aggressive weight quantization, metric-depth error is dominated by a
MULTIPLICATIVE global-SCALE drift, not by ADDITIVE relative-geometry change. If
true, and if the scale drift is generated by a small/identifiable set of layers,
protecting that "scale rail" at high precision recovers metric accuracy cheaply.

Method: weight-only per-output-channel symmetric FAKE quant (dequantized, run in
fp32) so we can sweep arbitrary bit-widths and quantize arbitrary subsets. Accuracy
is what the premise needs; real TRT latency comes later only if the premise holds.

For each config we report, vs the fp32 student reference (isolates quant effect):
  scale_drift   = |median(ref/quant) - 1|     (multiplicative term)
  geom_absrel   = abs_rel(ref, quant*scale)    (additive term, scale removed)
and vs GT depth (real-world impact):
  gt_absrel, gt_scale_err.
"""
