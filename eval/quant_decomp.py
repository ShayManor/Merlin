# Component name-fragment taxonomy (matches model/quant/quantize.py PROTECTED).
SCALE_RAIL = ("scale_head", "pose_head", "scale_token", "scale_adaptor", "pose_adaptor",
              "scale", "metric")
def comp_of(name):
    low = name
    if any(s in low for s in ("scale_head", "scale_token", "scale_adaptor", "pose_head",
                              "pose_adaptor", "depth_scale", "metric")):
        return "scale_rail"
    if low.startswith("encoder.") or ".encoder." in low:
        return "encoder"
    if "info_sharing" in low:
        return "info_sharing"
    if any(h in low for h in ("head", "dpt", "adaptor")):
        return "heads"
    return "other"


def linear_inventory(model):
    inv = {}
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear):
            c = comp_of(name)
            inv.setdefault(c, {"n": 0, "params": 0})
            inv[c]["n"] += 1
            inv[c]["params"] += mod.weight.numel()
    return inv


def fakequant_channel(w, bits):
    """Per-output-channel symmetric weight fake-quant -> dequantized tensor."""
    if bits >= 16:
        return w
    qmax = 2 ** (bits - 1) - 1
    amax = w.abs().amax(dim=1, keepdim=True).clamp_min(1e-12)
    scale = amax / qmax
    wq = torch.clamp(torch.round(w / scale), -qmax, qmax) * scale
    return wq


def fakeprune(w, sparsity):
    """Per-layer magnitude pruning: zero the smallest-|w| `sparsity` fraction."""
    if sparsity <= 0:
        return w
    k = int(w.numel() * sparsity)
    if k <= 0:
        return w
    thr = torch.kthvalue(w.abs().flatten(), k).values
    return torch.where(w.abs() > thr, w, torch.zeros_like(w))


def apply_prune(model, sparsity, select):
    saved = {}
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear) and select(name):
            saved[name] = mod.weight.data.clone()
            mod.weight.data.copy_(fakeprune(mod.weight.data.float(), sparsity).to(mod.weight.dtype))
    return saved


def fakequant_group(w, bits, group):
    """Per-INPUT-channel grouped symmetric weight fake-quant. Each (out, group)
    block gets its own scale -> handles input-channel (SmoothQuant-style) outliers
    that per-output-channel quant cannot. group divides in_features."""
    if bits >= 16:
        return w
    out_f, in_f = w.shape
    if in_f % group != 0:
        return fakequant_channel(w, bits)
    qmax = 2 ** (bits - 1) - 1
    wg = w.view(out_f, in_f // group, group)
    amax = wg.abs().amax(dim=2, keepdim=True).clamp_min(1e-12)
    scale = amax / qmax
    wq = torch.clamp(torch.round(wg / scale), -qmax, qmax) * scale
    return wq.view(out_f, in_f)


def apply_fakequant(model, bits, select, group=None):
    """In-place fake-quant of Linear weights where select(name) is True. Returns
    a dict of saved originals to restore. group=None -> per-output-channel."""
    saved = {}
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear) and select(name):
            saved[name] = mod.weight.data.clone()
            w = mod.weight.data.float()
            wq = fakequant_group(w, bits, group) if group else fakequant_channel(w, bits)
            mod.weight.data.copy_(wq.to(mod.weight.dtype))
    return saved


def apply_bitmap(model, bitmap_fn, group=None):
    """Fake-quant each Linear to bits=bitmap_fn(name); None/>=16 -> skip. For
    asymmetric per-component allocation."""
    saved = {}
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear):
            b = bitmap_fn(name)
            if b is None or b >= 16:
                continue
            saved[name] = mod.weight.data.clone()
            w = mod.weight.data.float()
            wq = fakequant_group(w, b, group) if group else fakequant_channel(w, b)
            mod.weight.data.copy_(wq.to(mod.weight.dtype))
    return saved


def avg_bits(model, bitmap_fn):
    """Param-weighted average bit-width over quantizable Linears (skipped -> 16)."""
    tot_p = tot_b = 0
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear):
            p = mod.weight.numel()
            b = bitmap_fn(name)
            tot_p += p
            tot_b += p * (16 if (b is None or b >= 16) else b)
    return round(tot_b / max(tot_p, 1), 2)


def weight_stats(model):
    """Per-component weight-outlier severity: kurtosis + per-input-channel spread
    (max column-amax / median column-amax). High input-channel spread => SmoothQuant
    -style outliers that per-output-channel quant cannot absorb."""
    import numpy as _np
    agg = {}
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear):
            c = comp_of(name)
            w = mod.weight.data.float()
            wn = w.cpu().numpy().ravel()
            kurt = float(((wn - wn.mean()) ** 4).mean() / (wn.var() ** 2 + 1e-12))
            col_amax = w.abs().amax(dim=0).cpu().numpy()        # per-input-channel
            spread = float(col_amax.max() / (_np.median(col_amax) + 1e-12))
            row_amax = w.abs().amax(dim=1).cpu().numpy()        # per-output-channel
            rspread = float(row_amax.max() / (_np.median(row_amax) + 1e-12))
            d = agg.setdefault(c, {"kurt": [], "in_spread": [], "out_spread": []})
            d["kurt"].append(kurt); d["in_spread"].append(spread); d["out_spread"].append(rspread)
    return {c: {k: round(float(_np.median(v)), 2) for k, v in d.items()} for c, d in agg.items()}


def restore(model, saved):
    d = dict(model.named_modules())
    for name, w in saved.items():
        d[name].weight.data.copy_(w)


def _quant_tensor(x, bits, per_token):
    if bits >= 16:
        return x
    qmax = 2 ** (bits - 1) - 1
    if per_token:                       # per-row (last-dim) symmetric, transformer-standard
        amax = x.abs().amax(dim=-1, keepdim=True).clamp_min(1e-12)
    else:                               # per-tensor (naive)
        amax = x.abs().amax().clamp_min(1e-12)
    s = amax / qmax
    return torch.clamp(torch.round(x / s), -qmax, qmax) * s


def add_act_hooks(model, bits, select, per_token=True):
    """Fake-quant the INPUT activation of each Linear where select(name). Returns
    handles to remove."""
    handles = []
    def mk():
        def hook(mod, inp):
            x = inp[0]
            return (_quant_tensor(x.float(), bits, per_token).to(x.dtype),) + inp[1:]
        return hook
    for name, mod in model.named_modules():
        if isinstance(mod, torch.nn.Linear) and select(name):
            handles.append(mod.register_forward_pre_hook(mk()))
    return handles


import re as _re
def enc_block_idx(name):
    m = _re.search(r"block[s]?\.(\d+)\.", name)
    return int(m.group(1)) if m else -1


@torch.no_grad()
def infer_depths(model, frames, dev, res):
    model.eval()
    s = (res // 14) * 14
    out = []
    for rp, dp in frames:
        view = make_views([rp], s, dev)
        pr = model(view, memory_efficient_inference=True, minibatch_size=1)[0]
        pz = pr["pts3d_cam"][0, ..., 2].float().cpu().numpy()
        out.append(pz)
    return out


def gt_depths(frames, shapes, data="tum"):
    import cv2
    g = []
    for (rp, dp), sh in zip(frames, shapes):
        if data == "habitat":
            d = np.load(dp).astype(np.float32)
            if d.shape != tuple(sh):
                d = cv2.resize(d, (sh[1], sh[0]), interpolation=cv2.INTER_NEAREST)
            g.append(d)
        else:
            g.append(load_gt_depth(dp, sh[0], sh[1]))
    return g


def fwd_range(d):
    """Planner-consumed metric: nearest-obstacle range (m) in the central forward
    corridor (the range a reactive/Nav2 planner actually brakes on). 10th-pct of valid
    depths in the lower-center image region."""
    H, W = d.shape
    corr = d[int(H * 0.45):int(H * 0.9), int(W * 0.38):int(W * 0.62)]
    v = corr[np.isfinite(corr) & (corr > 0.1) & (corr < 15)]
    return float(np.percentile(v, 10)) if v.size > 50 else float("nan")


def decompose(ref_list, q_list, gt_list):
    """Aggregate (median over frames) the scale vs geometry decomposition."""
    sd, ga, gt_ar, gt_sc, gt_al = [], [], [], [], []
    sr = []                           # signed scale ratio q/ref (directional bias test)
    gt_al5, gt_al10 = [], []          # IMU-recovered with realistic 5%/10% scale-estimate noise
    fre = []                          # forward-corridor range error (m), IMU-recovered
    rng = np.random.default_rng(0)
    for ref, q, gt in zip(ref_list, q_list, gt_list):
        m = np.isfinite(ref) & np.isfinite(q) & (ref > 1e-3) & (q > 1e-3)
        if m.sum() < 200:
            continue
        s = np.median(ref[m] / q[m])
        sd.append(abs(s - 1.0))                                   # scale drift vs ref
        sr.append(float(np.median(q[m] / ref[m])))               # SIGNED ratio q/ref (<1 = compression shrinks depth)
        qa = q * s
        ga.append(depth_metrics(ref, qa)["abs_rel"])             # geometry drift vs ref
        gt_ar.append(depth_metrics(gt, q)["abs_rel"])            # raw metric abs_rel vs GT
        gt_sc.append(scale_error(gt, q))                         # scale err vs GT
        # IMU-recovered metric error: re-anchor global scale (an external IMU module
        # recovers metric scale to <5%, C2), so this is the achievable metric accuracy
        # when only relative geometry must survive quantization.
        mg = np.isfinite(gt) & np.isfinite(q) & (gt > 1e-3) & (q > 1e-3)
        if mg.sum() >= 200:
            sg = np.median(gt[mg] / q[mg])
            gt_al.append(depth_metrics(gt, q * sg)["abs_rel"])
            # realistic IMU scale-estimate error (C2: ~5%): perturb the recovered scale
            gt_al5.append(depth_metrics(gt, q * sg * (1 + 0.05 * rng.standard_normal()))["abs_rel"])
            gt_al10.append(depth_metrics(gt, q * sg * (1 + 0.10 * rng.standard_normal()))["abs_rel"])
            # planner-consumed forward-range error (m) on the IMU-recovered depth
            fr_q, fr_g = fwd_range(q * sg), fwd_range(gt)
            if np.isfinite(fr_q) and np.isfinite(fr_g):
                fre.append(abs(fr_q - fr_g))
    f = lambda x: round(float(np.nanmedian(x)), 4) if x else None
    return {"scale_drift_vs_ref": f(sd), "scale_ratio_qref": f(sr), "geom_absrel_vs_ref": f(ga),
            "gt_absrel": f(gt_ar), "gt_absrel_imu": f(gt_al), "gt_absrel_imu5": f(gt_al5),
            "gt_absrel_imu10": f(gt_al10), "gt_fwdrange_err_m": f(fre),
            "gt_scale_err": f(gt_sc), "n": len(sd)}


def get_frames(tum_root, seqs, stride, limit, data="tum", data_root=None):
    if data == "habitat":
        import glob
        rgbs = sorted(glob.glob(os.path.join(data_root, "rgb_*.png")))[::stride]
        frames = [(r, r.replace("rgb_", "dep_").replace(".png", ".npy")) for r in rgbs]
        return frames[:limit]
    frames = []
    for sq in seqs:
        d = os.path.join(tum_root, sq)
        pairs = tum_pairs(d)
        frames += pairs[::stride]
    return frames[:limit]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--ckpt", default="/workspace/ckpt/student_v3.pt")
    ap.add_argument("--tum-root", default="/workspace/data/tum")
    ap.add_argument("--seqs", default="rgbd_dataset_freiburg1_xyz,rgbd_dataset_freiburg1_plant")
    ap.add_argument("--stride", type=int, default=15)
    ap.add_argument("--limit", type=int, default=60)
    ap.add_argument("--res", type=int, default=378)
    ap.add_argument("--mode", default="uniform",
                    choices=["inventory", "uniform", "component", "stats", "recipe",
                             "actquant", "enclayers", "prune"])
    ap.add_argument("--bits", type=int, default=3, help="for component mode")
    ap.add_argument("--out", default="/workspace/ckpt/quant_decomp.json")
    ap.add_argument("--data", default="tum", choices=["tum", "habitat"])
    ap.add_argument("--data-root", default="/workspace/data/habitat/val")
    ap.add_argument("--teacher", action="store_true", help="decompose the 1.23B teacher (ViT-G)")
    ap.add_argument("--hf-id", default="facebook/map-anything-apache")
    args = ap.parse_args()
    dev = "cuda"

    if args.teacher:
        from mapanything.models import MapAnything
        sys.path.insert(0, os.path.join(HERE, "..", "model", "backbones"))
        from teacher import _upcast_geometry_postproc
        student = MapAnything.from_pretrained(args.hf_id).to(dev).eval()
        _upcast_geometry_postproc()
        print("[teacher] loaded MapAnything 1.23B (fp32, ViT-G encoder)")
    else:
        student = build_student(size="base", aat_depth=8, device="cpu")
        ck = torch.load(args.ckpt, map_location="cpu", weights_only=False)
        student.load_state_dict(ck["state_dict"]); student = student.to(dev)

    inv = linear_inventory(student)
    print("[inventory] linear modules by component:")
    for c, d in sorted(inv.items()):
        print(f"  {c:14s} n={d['n']:4d}  params={d['params']/1e6:7.2f}M")
    if args.mode == "inventory":
        json.dump({"inventory": inv}, open(args.out, "w"), indent=2)
        print(f"[saved] {args.out}"); return

    if args.mode == "stats":
        ws = weight_stats(student)
        print("[weight-stats] component: kurtosis / in-channel-spread / out-channel-spread")
        for c, d in sorted(ws.items()):
            print(f"  {c:14s} kurt={d['kurt']:8.2f}  in_spread={d['in_spread']:8.2f}  out_spread={d['out_spread']:8.2f}")
        json.dump({"weight_stats": ws, "inventory": inv}, open(args.out, "w"), indent=2)
        print(f"[saved] {args.out}"); return

    frames = get_frames(args.tum_root, args.seqs.split(","), args.stride, args.limit,
                        data=args.data, data_root=args.data_root)
    print(f"[frames] {len(frames)} held-out {args.data} frames")
    ref = infer_depths(student, frames, dev, args.res)
    shapes = [r.shape for r in ref]
    gt = gt_depths(frames, shapes, data=args.data)
    base = decompose(ref, [r.copy() for r in ref], gt)  # ref vs ref = 0 drift; gt_* is the fp32 floor
    print("[fp32 ref] vs-GT floor:", base)

    results = {"meta": {"ckpt": args.ckpt, "n_frames": len(frames), "res": args.res,
                        "inventory": inv, "fp32_floor": base}, "runs": []}

    if args.mode == "uniform":
        configs = [("all", b, lambda n: True) for b in (8, 6, 5, 4, 3, 2)]
    else:  # component, fixed bits
        b = args.bits
        configs = [
            ("all", b, lambda n: True),
            ("protect_scale_rail", b, lambda n: comp_of(n) != "scale_rail"),
            ("only_scale_rail", b, lambda n: comp_of(n) == "scale_rail"),
            ("only_encoder", b, lambda n: comp_of(n) == "encoder"),
            ("only_info_sharing", b, lambda n: comp_of(n) == "info_sharing"),
            ("only_heads", b, lambda n: comp_of(n) == "heads"),
        ]

    if args.mode == "prune":
        # Does the IMU-anchored insight generalize beyond quantization? Magnitude-prune
        # and decompose scale vs geometry + IMU-recovery.
        for sp in (0.3, 0.5, 0.7, 0.8, 0.9, 0.95):
            saved = apply_prune(student, sp, lambda n: True)
            q = infer_depths(student, frames, dev, args.res)
            restore(student, saved)
            d = decompose(ref, q, gt)
            d.update({"tag": f"prune_{sp}", "sparsity": sp})
            results["runs"].append(d)
            print(f"[prune] sparsity={sp}  scale_drift={d['scale_drift_vs_ref']}  "
                  f"geom={d['geom_absrel_vs_ref']}  gt_naive={d['gt_absrel']}  "
                  f"gt_imu={d['gt_absrel_imu']}", flush=True)
        json.dump(results, open(args.out, "w"), indent=2)
        print(f"[saved] {args.out}"); return

    if args.mode == "actquant":
        # Weight + activation fake-quant (realistic TRT-INT8-style). Tests whether the
        # encoder-bottleneck finding survives activation quant, and where it localizes.
        ALL = lambda n: True
        ENC = lambda n: comp_of(n) == "encoder"
        AAT = lambda n: comp_of(n) == "info_sharing"
        configs = [
            # tag, w_bits, w_sel, a_bits, a_sel, per_token
            ("W8A8_all",      8, ALL, 8, ALL, True),
            ("W4A8_all",      4, ALL, 8, ALL, True),
            ("W3A8_all",      3, ALL, 8, ALL, True),
            ("W8A4_all",      8, ALL, 4, ALL, True),
            ("W8A8pt_all",    8, ALL, 8, ALL, False),   # per-tensor act (naive)
            ("W4A4_all",      4, ALL, 4, ALL, True),
            ("W4A4_encOnly",  4, ENC, 4, ENC, True),
            ("W4A4_aatOnly",  4, AAT, 4, AAT, True),
            ("W8A4_encOnly",  8, ALL, 4, ENC, True),    # A4 only on encoder
            ("W8A4_aatOnly",  8, ALL, 4, AAT, True),    # A4 only on AAT
        ]
        for tag, wb, wsel, ab, asel, pt in configs:
            saved = apply_fakequant(student, wb, wsel)
            hooks = add_act_hooks(student, ab, asel, per_token=pt)
            q = infer_depths(student, frames, dev, args.res)
            for h in hooks: h.remove()
            restore(student, saved)
            d = decompose(ref, q, gt)
            d.update({"tag": tag, "w_bits": wb, "a_bits": ab, "per_token": pt})
            results["runs"].append(d)
            print(f"[actq] {tag:16s} W{wb}A{ab} pt={pt}  geom={d['geom_absrel_vs_ref']}  "
                  f"scale={d['scale_drift_vs_ref']}  gtabs={d['gt_absrel']}", flush=True)
        json.dump(results, open(args.out, "w"), indent=2)
        print(f"[saved] {args.out}"); return

    if args.mode == "enclayers":
        # Per-encoder-block weight quant @args.bits: is the fragility a few blocks?
        idxs = sorted(set(enc_block_idx(n) for n, m in student.named_modules()
                          if isinstance(m, torch.nn.Linear) and comp_of(n) == "encoder"
                          and enc_block_idx(n) >= 0))
        print(f"[enclayers] encoder blocks: {idxs}")
        for bi in idxs:
            sel = lambda n, bi=bi: comp_of(n) == "encoder" and enc_block_idx(n) == bi
            saved = apply_fakequant(student, args.bits, sel)
            q = infer_depths(student, frames, dev, args.res)
            restore(student, saved)
            d = decompose(ref, q, gt)
            d.update({"tag": f"encblock_{bi}", "bits": args.bits, "n_quantized": len(saved)})
            results["runs"].append(d)
            print(f"[enclayers] block {bi:2d} b={args.bits} nq={len(saved)}  "
                  f"geom={d['geom_absrel_vs_ref']}  scale={d['scale_drift_vs_ref']}", flush=True)
        json.dump(results, open(args.out, "w"), indent=2)
        print(f"[saved] {args.out}"); return

    if args.mode == "recipe":
        # Asymmetric per-component allocation + group-quant mechanism test.
        # bitmap functions: name -> bits (None=skip/fp16).
        def bm(enc, aat, head, rail):
            def f(n):
                c = comp_of(n)
                return {"encoder": enc, "info_sharing": aat, "heads": head,
                        "scale_rail": rail, "other": head}.get(c, head)
            return f
        recipes = [
            # tag, bitmap, group  -- baselines
            ("uniform_w8", bm(8, 8, 8, 8), None),
            ("uniform_w4", bm(4, 4, 4, 4), None),
            ("uniform_w3", bm(3, 3, 3, 3), None),
            # asymmetric: cheap 3D stack, protected encoder
            ("enc8_rest3", bm(8, 3, 3, 3), None),
            ("enc8_rest2", bm(8, 2, 2, 2), None),
            ("enc6_rest3", bm(6, 3, 3, 3), None),
            ("enc4_rest3", bm(4, 3, 3, 3), None),
            # mechanism: does input-channel group quant rescue the encoder @3?
            ("uniform_w3_g64", bm(3, 3, 3, 3), 64),
            ("enc3g64_rest3", bm(3, 3, 3, 3), 64),
        ]
        for tag, bmap, group in recipes:
            saved = apply_bitmap(student, bmap, group=group)
            q = infer_depths(student, frames, dev, args.res)
            restore(student, saved)
            d = decompose(ref, q, gt)
            d.update({"tag": tag, "avg_bits": avg_bits(student, bmap), "group": group,
                      "n_quantized": len(saved)})
            results["runs"].append(d)
            print(f"[recipe] {tag:18s} avgb={d['avg_bits']:5.2f} g={group}  "
                  f"scale_drift={d['scale_drift_vs_ref']}  geom_absrel={d['geom_absrel_vs_ref']}  "
                  f"gt_absrel={d['gt_absrel']}  gt_scale_err={d['gt_scale_err']}", flush=True)
        json.dump(results, open(args.out, "w"), indent=2)
        print(f"[saved] {args.out}"); return

    for tag, b, sel in configs:
        saved = apply_fakequant(student, b, sel)
        q = infer_depths(student, frames, dev, args.res)
        restore(student, saved)
        d = decompose(ref, q, gt)
        d.update({"tag": tag, "bits": b, "n_quantized": len(saved)})
        results["runs"].append(d)
        print(f"[{args.mode}] {tag:20s} b={b} nq={len(saved):4d}  "
              f"scale_drift={d['scale_drift_vs_ref']}  geom_absrel={d['geom_absrel_vs_ref']}  "
              f"gt_absrel={d['gt_absrel']}  gt_scale_err={d['gt_scale_err']}", flush=True)

    json.dump(results, open(args.out, "w"), indent=2)
    print(f"[saved] {args.out}")


if __name__ == "__main__":
    main()
